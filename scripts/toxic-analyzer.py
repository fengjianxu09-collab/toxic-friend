def generate_toxic(problem, intensity):
    """生成毒舌回复"""
    if problem not in TEMPLATES:
        return "问题类型不支持，请尝试：送人头、打野送、操作菜、意识差、迷之自信"
    
    if intensity not in TEMPLATES[problem]:
        return "强度不支持，请尝试：轻微、中等、重度、极致"
    
    roast = random.choice(TEMPLATES[problem][intensity])
    suggestion = SUGGESTIONS[problem]
    motivation = random.choice(MOTIVATIONS)
    
    return f"""
【毒舌】{roast}
【建议】{suggestion}
【激励】{motivation}
"""


def main():
    if len(sys.argv) < 3:
        print("用法：python toxic-analyzer.py <问题> <强度>")
        print("问题：送人头、打野送、操作菜、意识差、迷之自信")
        print("强度：轻微、中等、重度、极致")
        sys.exit(1)
    
    problem = sys.argv[1]
    intensity = sys.argv[2]
    
    response = generate_toxic(problem, intensity)
    print(response)


if __name__ == "__main__":
    main()
